
public class ForExemplo2 {
		
	public static void main(String[] args) {
			
			int i = 0, j = 0;
			// for (inicialização, condição e incremento)
			System.out.println("Início. Não vai se repetir");
			for (i = 0; i < 5; i++) {
			    System.out.printf("Valor de i: %d. \n", i);
			    for (j = 0; j < 3; j++){
			        System.out.printf("     Valor de j: %d. \n", j);
			    }
			        
			}
		}
	}
